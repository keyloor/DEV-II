package com.practica3.Taco;

public class Salsa {
    private String name;
    private String spiciness;

    public Salsa() {}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSpiciness() {
        return spiciness;
    }

    public void setSpiciness(String spiciness) {
        this.spiciness = spiciness;
    }
}
